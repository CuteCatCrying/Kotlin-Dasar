fun main() {
    val result1: Int = 10 / 3
    println(result1)

    val result2: Int = 10 + 10 / 2
    println(result2)

    val result3: Double = 10 / 3.0
    println(result3)

    // Augmented assignments
    var a = 10
    println(a)
    a += 10
    println(a)

    // Unary operator
    var b = -5
    b++
    println(b)
    b--
    println(b)
}